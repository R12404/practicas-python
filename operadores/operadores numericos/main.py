# Restando dos variables

resta = operandoA - operandoB
print( f'Resultado de la resta: {resta}')

# Multiplicando dos variables

multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicación: {multiplicacion}')

# Dividiendo dos variables
division = operandoA / operandoB
print(f'Resultado de la división: {division}')

# Dividiendo dos variables pero obteniendo solo su parte entera
divisionEntera = operandoA // operandoB
print( f'Resultado de la division: {divisionEntera}')

# Residuo de dividir dos variables

residuo = operandoA % operandoB
print( f'El residuo de la division es: {residuo}')

# Exponente de dos numeros 
exponente = 3 ** 2
print(f'Resultado exponente: {exponente}')